    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
       String name =tfname.getText();
       String email =tfemail.getText();
       String phone = tfphone.getText();
       String address = tfaddress.getText();
       String dateofbirth=tfdateofbirth.getText();
       String nationality = tfnationality.getText();
       String id =tfid.getText();
       String hobbies = tfhobbies.getText();
       
       
       if(name.isEmpty() || email.isEmpty() || phone.isEmpty() ||address.isEmpty() || dateofbirth.isEmpty() || nationality.isEmpty() || id.isEmpty() || hobbies.isEmpty()){
       JOptionPane.showMessageDialog(this,"please enter all fields","try again",JOptionPane.ERROR_MESSAGE);
       }
       else {
               DefaultTableModel model = (DefaultTableModel) tableStudents.getModel();
               model.addRow(new Object[]{name,email,phone,address,dateofbirth,nationality,id,hobbies});
               tfname.setText("");
               tfemail.setText("");
               tfphone.setText("");
               tfaddress.setText("");
               tfdateofbirth.setText("");
               tfnationality.setText("");
               tfid.setText("");
               tfhobbies.setText("");
       }
        
    }//GEN-LAST:event_btnaddActionPerformed

